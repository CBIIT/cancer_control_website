$(document).ready(function() {
	var p_div = $('#popup'),
		a_div = $('#popup-acro'),
		c_div = $('.contact-info');

	// user interaction (image) - hover
	$('area').mouseenter(function(e) {
		var x = $(this).attr('id'),
			term_content = '',
			moveLeft = 0,
			moveDown = 0;
						
		if (x === 'psycho-social' || x === 'quality-of-life') {
			moveLeft = -310,
			moveDown = 70;
		} else if (x === 'health-behaviors' || x === 'disease-progression') {
			moveLeft = -600,
			moveDown = -225;
		} else if (x === 'research-platforms') {
			moveLeft = 50,
			moveDown = -150;
		} else if (x === 'special-populations' || x === 'patterns' || x === 'economic-impact') {
			moveLeft = 20,
			moveDown = -25;
		} else {
			moveLeft = -140,
			moveDown = 30;
		}
		
		if (windowsize < 768) {
			if (x === 'health-behaviors' || x === 'disease-progression') {
			moveLeft = -400,
			moveDown = -150;
			} else if (x === 'research-platforms') {
			moveLeft = 100,
			moveDown = -250;
			} else {
			}
		}

			if (windowsize > 490) {
		for(var i = 0; i < terms.length; i++) {
			if(terms[i].term == x) { term_content += '<h3>' + terms[i].title + '</h3>' + '<p>' + terms[i].definition + '</p>' }
		}
		p_div.empty();
		p_div.append(term_content);
		p_div.stop(true).fadeTo(0, 1.0);

		$('area').mousemove(function(e) { p_div.css('top', e.pageY + moveDown).css('left', e.pageX + moveLeft) });
	}});
	$('area').mouseleave(function(e) { p_div.fadeOut(0) });

	// user interaction - click
	$('area').click(function() {
		var y = $(this).attr('id');
		for(var i = 0; i < terms.length; i++) {
			if(terms[i].term == y) {
				contact_content = '<h2>' + terms[i].title + '</h2>';
				contact_content += terms[i].contacts;
			}
		}
		c_div.empty();
		c_div.append(contact_content);
	});
});

// scroll animation
$(function() {
	$('area[href*=#]:not([href=#])').click(function() {
		if (location.pathname.replace(/^\//,'') == this.pathname.replace(/^\//,'') && location.hostname == this.hostname) {
			var target = $(this.hash);
			target = target.length ? target : $('[name=' + this.hash.slice(1) +']');
			if (target.length) {
				$('html,body').animate({ scrollTop: target.offset().top }, 1200);
				return false;
			}
		}
	});
});